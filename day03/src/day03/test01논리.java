package day03;

public class test01논리 {
	public static void main(String[] args) {
		
		//논리
		//-숫자가 아닌 데이터
		//-true와false 딱 두가지만 저장 할 수 있는 령태
		
		boolean a =true;
		boolean b =false;
		System.out.println(a);
		System.out.println(b);
		
		
		//-계산을 통해 프로그램으로 하여금 판정을 내리고 결과를 저장하기 위한 형태
		
		//(ex)비싸다,키가크다,과체중이다,멀다,작다,좁다,잘생겼다
		
		int pizza =35000;
		int pigFeet=32000;
		//boolean expensive=pizza의 값이 pigFeet 의 값보다 큰가요;
		boolean expensive =pizza > pigFeet;
		 System.out.println(expensive);
		 
		 //비교 연산 -숫자 두개를 비교하여 논리를 만드는 연산 
		 //종류-크다(>),작다(<),크거나 같다(>+),작거나 같다(<=),같다(==),
		
	}

}
